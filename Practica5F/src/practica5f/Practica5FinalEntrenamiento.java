    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        if(banStop){
            creaHilos();
            System.out.println("Creo hilos");
        }
        banStop=false;
        if(CC.isSelected()){
            cambioAlgoritmo(0);
        }
        if(DI.isSelected()){
            cambioAlgoritmo(1);
        }
        if(VC.isSelected()){
            cambioAlgoritmo(2);
        }
        if(DK.isSelected()){
            cambioAlgoritmo(3);
        }
        if(DJ.isSelected()){
            cambioAlgoritmo(4);
        }
        if(MX.isSelected()){
            cambioAlgoritmo(5);
        }
        limpiaTexto();
        t1.start();
        t2.start();
        t3.start();
        t4.start();
        btnCrear.setEnabled(false);
        if(CC.isSelected())
            btnKill.setEnabled(false);
        else
            btnKill.setEnabled(true);
    }//GEN-LAST:event_btnCrearActionPerformed

    public void limpiaTexto(){
        area1.setText("");
        area2.setText("");
        area3.setText("");
        area4.setText("");
    }
    
    private void btnKillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKillActionPerformed
        int HiloRand = (int)(Math.random()*4);
        switch(HiloRand){
            case 0:
                try{
                    t1.setDead(true);
                }catch(Exception e){JOptionPane.showMessageDialog(null,"Error al suspender el hilo");}
                break;
            case 1:
                try{
                    t2.setDead(true);
                }catch(Exception e){JOptionPane.showMessageDialog(null,"Error al suspender el hilo");}
                break;
            case 2:
                try{
                    t3.setDead(true);
                }catch(Exception e){JOptionPane.showMessageDialog(null,"Error al suspender el hilo");}
                break;
            case 3:
                try{
                    t4.setDead(true);
                }catch(Exception e){JOptionPane.showMessageDialog(null,"Error al suspender el hilo");}
                break;
            default:
                break;
        }
    }//GEN-LAST:event_btnKillActionPerformed


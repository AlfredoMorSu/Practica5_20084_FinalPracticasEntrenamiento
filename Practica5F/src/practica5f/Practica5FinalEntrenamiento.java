    public void mataResto(){
        if(!t1.isDead())
            t1.stop();
        if(!t2.isDead())
            t2.stop();
        if(!t3.isDead())
            t3.stop();
        if(!t4.isDead())
            t4.stop();
        banStop=true;
    }
